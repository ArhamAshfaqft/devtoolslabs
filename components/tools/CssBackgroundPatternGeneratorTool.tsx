function generateParticleCode(c: EffectConfig): string {
  return `<!-- Particle Network Background — Generated by DevToolsLabs.com -->
<div id="particle-bg" style="position:fixed;top:0;left:0;width:100%;height:100%;z-index:-1;background:${c.bgColor};">
  <canvas id="particle-canvas" style="display:block;width:100%;height:100%;"></canvas>
</div>
<script>
(function(){
  const canvas = document.getElementById('particle-canvas');
  const ctx = canvas.getContext('2d');
  let w, h, particles = [], mouse = {x:-1000,y:-1000};
  const COLORS = ['${c.colorA}','${c.colorB}','${c.colorC}'];
  const COUNT = ${c.density};
  const SPEED = ${c.speed};
  const CONNECT = ${c.connectDistance};

  function resize(){ w=canvas.width=canvas.offsetWidth; h=canvas.height=canvas.offsetHeight; }
  window.addEventListener('resize', resize); resize();
  canvas.addEventListener('mousemove', e=>{ const r=canvas.getBoundingClientRect(); mouse={x:e.clientX-r.left,y:e.clientY-r.top}; });
  canvas.addEventListener('mouseleave', ()=>{ mouse={x:-1000,y:-1000}; });

  for(let i=0;i<COUNT;i++) particles.push({
    x:Math.random()*w, y:Math.random()*h,
    vx:(Math.random()-0.5)*SPEED, vy:(Math.random()-0.5)*SPEED,
    r:Math.random()*2+1, color:COLORS[i%3]
  });

  function hexToRgb(hex){
    const r=parseInt(hex.slice(1,3),16), g=parseInt(hex.slice(3,5),16), b=parseInt(hex.slice(5,7),16);
    return r+','+g+','+b;
  }

  function draw(){
    ctx.clearRect(0,0,w,h);
    for(let i=0;i<particles.length;i++){
      const p=particles[i];
      p.x+=p.vx; p.y+=p.vy;
      if(p.x<0||p.x>w) p.vx*=-1;
      if(p.y<0||p.y>h) p.vy*=-1;
      // Mouse attraction
      const dx=mouse.x-p.x, dy=mouse.y-p.y, dist=Math.sqrt(dx*dx+dy*dy);
      if(dist<150){ p.x+=dx*0.01; p.y+=dy*0.01; }
      ctx.beginPath(); ctx.arc(p.x,p.y,p.r,0,Math.PI*2);
      ctx.fillStyle=p.color; ctx.fill();
      // Connections
      for(let j=i+1;j<particles.length;j++){
        const q=particles[j], ddx=p.x-q.x, ddy=p.y-q.y, d=Math.sqrt(ddx*ddx+ddy*ddy);
        if(d<CONNECT){
          ctx.beginPath(); ctx.moveTo(p.x,p.y); ctx.lineTo(q.x,q.y);
          ctx.strokeStyle='rgba('+hexToRgb(p.color)+','+(1-d/CONNECT)*0.4+')';
          ctx.lineWidth=0.6; ctx.stroke();
        }
      }
    }
    requestAnimationFrame(draw);
  }
  draw();
})();
</script>`;
}

