function getElementorJS(effect: EffectType, c: EffectConfig): string {
  const className = getElementorClassName(effect);
  // Build the inner drawing logic for each effect
  const drawLogic = getCanvasDrawLogic(effect, c);
  
  return `<!-- Site Script — Generated by DevToolsLabs.com -->
<!-- Paste ONCE into: WPCode plugin / Theme Header / Elementor → Custom Code -->
<script>
document.addEventListener('DOMContentLoaded', function() {
  document.querySelectorAll('.${className}').forEach(function(section, idx) {
    // Setup container
    section.style.position = 'relative';
    section.style.overflow = 'hidden';

    // Create canvas
    var wrapper = document.createElement('div');
    wrapper.style.cssText = 'position:absolute;top:0;left:0;width:100%;height:100%;z-index:0;pointer-events:none;';
    var canvas = document.createElement('canvas');
    canvas.style.cssText = 'display:block;width:100%;height:100%;';
    canvas.id = 'dtl-canvas-' + idx;
    wrapper.appendChild(canvas);
    section.insertBefore(wrapper, section.firstChild);

    // Ensure content is above
    Array.from(section.children).forEach(function(child) {
      if (child !== wrapper) child.style.position = child.style.position || 'relative';
    });

${drawLogic}
  });
});
</script>`;
}

function getCanvasDrawLogic(effect: EffectType, c: EffectConfig): string {
  if (effect === 'gradient-blobs') return '    // Pure CSS effect — no JS needed!';
  
  if (effect === 'particles') return `    // Particle Network
    var ctx = canvas.getContext('2d');
    var w, h, particles = [], mouse = {x:-1000,y:-1000};
    var COLORS = ['${c.colorA}','${c.colorB}','${c.colorC}'];
    var COUNT = ${c.density}, SPEED = ${c.speed}, CONNECT = ${c.connectDistance};

    function resize(){
      var rect = section.getBoundingClientRect();
      w = rect.width; h = rect.height;
      var dpr = window.devicePixelRatio || 1;
      canvas.width = w * dpr; canvas.height = h * dpr;
      ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
    }
    window.addEventListener('resize', resize); resize();
    section.addEventListener('mousemove', function(e){ var r=section.getBoundingClientRect(); mouse={x:e.clientX-r.left,y:e.clientY-r.top}; });
    section.addEventListener('mouseleave', function(){ mouse={x:-1000,y:-1000}; });

    for(var i=0;i<COUNT;i++) particles.push({
      x:Math.random()*w, y:Math.random()*h,
      vx:(Math.random()-0.5)*SPEED, vy:(Math.random()-0.5)*SPEED,
      color:COLORS[i%3], r:1.5+Math.random()*2
    });

    (function draw(){
      ctx.clearRect(0,0,w,h);
      ctx.fillStyle='${c.bgColor}'; ctx.fillRect(0,0,w,h);
      for(var i=0;i<particles.length;i++){
        var p=particles[i];
        p.x+=p.vx; p.y+=p.vy;
        if(p.x<0||p.x>w) p.vx*=-1;
        if(p.y<0||p.y>h) p.vy*=-1;
        var dm=Math.hypot(p.x-mouse.x,p.y-mouse.y);
        if(dm<150){ p.x+=(p.x-mouse.x)*0.02; p.y+=(p.y-mouse.y)*0.02; }
        ctx.beginPath(); ctx.arc(p.x,p.y,p.r,0,Math.PI*2);
        ctx.fillStyle=p.color; ctx.fill();
        for(var j=i+1;j<particles.length;j++){
          var p2=particles[j], d=Math.hypot(p.x-p2.x,p.y-p2.y);
          if(d<CONNECT){ ctx.beginPath(); ctx.moveTo(p.x,p.y); ctx.lineTo(p2.x,p2.y);
            ctx.strokeStyle=p.color.replace(')',','+(1-d/CONNECT)*0.3+')').replace('rgb','rgba');
            ctx.lineWidth=0.5; ctx.stroke(); }
        }
      }
      requestAnimationFrame(draw);
    })();`;

  if (effect === 'matrix-rain') return `    // Matrix Rain
    var ctx = canvas.getContext('2d');
    var w, h, columns, drops;
    var CHARS = 'ABCDEFGHIJKLMNOPQRSTUVWXYZアイウエオカキクケコ0123456789@#$%^&*';
    var FONT = 14, SPEED = ${c.speed};
    var mouse = {x:-1000, y:-1000};
    function hexToRgb(hex){ return [parseInt(hex.slice(1,3),16),parseInt(hex.slice(3,5),16),parseInt(hex.slice(5,7),16)]; }
    var mainColor = hexToRgb('${c.colorA}'), glowColor = hexToRgb('${c.colorB}'), bgRgb = hexToRgb('${c.bgColor}');

    function resize(){
      var rect = section.getBoundingClientRect();
      w = rect.width; h = rect.height;
      var dpr = window.devicePixelRatio || 1;
      canvas.width = w * dpr; canvas.height = h * dpr;
      ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
      columns=Math.ceil(w/FONT);
      drops=Array.from({length:columns},function(){ return Math.random()*-50; });
    }
    window.addEventListener('resize', resize); resize();
    section.addEventListener('mousemove', function(e){ var r=section.getBoundingClientRect(); mouse={x:e.clientX-r.left,y:e.clientY-r.top}; });
    ctx.fillStyle='rgb('+bgRgb[0]+','+bgRgb[1]+','+bgRgb[2]+')'; ctx.fillRect(0,0,w,h);

    (function draw(){
      ctx.fillStyle='rgba('+bgRgb[0]+','+bgRgb[1]+','+bgRgb[2]+',0.05)'; ctx.fillRect(0,0,w,h);
      for(var i=0;i<columns;i++){
        var x=i*FONT, y=drops[i]*FONT;
        var ch=CHARS[Math.floor(Math.random()*CHARS.length)];
        var dx=x-mouse.x, dy=y-mouse.y, dist=Math.sqrt(dx*dx+dy*dy);
        var glow=dist<120?(1-dist/120):0;
        if(glow>0.1){ ctx.font='bold '+(FONT+2)+'px monospace'; ctx.fillStyle='rgba('+glowColor[0]+','+glowColor[1]+','+glowColor[2]+','+(0.9+glow*0.1)+')'; ctx.shadowBlur=20*glow; ctx.shadowColor='${c.colorB}'; }
        else { ctx.font=FONT+'px monospace'; ctx.fillStyle='rgba('+mainColor[0]+','+mainColor[1]+','+mainColor[2]+','+(0.5+Math.random()*0.4)+')'; ctx.shadowBlur=0; }
        ctx.fillText(ch,x,y); ctx.shadowBlur=0;
        if(y>h&&Math.random()>0.975) drops[i]=0;
        drops[i]+=SPEED*0.5;
      }
      requestAnimationFrame(draw);
    })();`;

  if (effect === 'waves') return `    // Wave Gradient
    var ctx = canvas.getContext('2d');
    var w, h, t=0, SPEED = ${c.speed * 0.01};
    var mouse = {x:0.5, y:0.5};
    function hexToRgba(hex,a){ var r=parseInt(hex.slice(1,3),16),g=parseInt(hex.slice(3,5),16),b=parseInt(hex.slice(5,7),16); return 'rgba('+r+','+g+','+b+','+a+')'; }
    function resize(){
      var rect = section.getBoundingClientRect();
      w = rect.width; h = rect.height;
      var dpr = window.devicePixelRatio || 1;
      canvas.width = w * dpr; canvas.height = h * dpr;
      ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
    }
    window.addEventListener('resize', resize); resize();
    section.addEventListener('mousemove', function(e){ var r=section.getBoundingClientRect(); mouse={x:(e.clientX-r.left)/w,y:(e.clientY-r.top)/h}; });
    var waves = [
      { color:'${c.colorA}', amp:50, freq:0.8, speed:1, yOff:0.6, opacity:0.4 },
      { color:'${c.colorB}', amp:40, freq:1.2, speed:1.3, yOff:0.7, opacity:0.35 },
      { color:'${c.colorC}', amp:30, freq:1.6, speed:0.8, yOff:0.8, opacity:0.3 },
      { color:'${c.colorA}', amp:25, freq:2.0, speed:1.5, yOff:0.85, opacity:0.25 }
    ];
    (function draw(){
      ctx.clearRect(0,0,w,h); ctx.fillStyle='${c.bgColor}'; ctx.fillRect(0,0,w,h); t+=SPEED;
      var mouseInfluence = (mouse.x-0.5)*30;
      waves.forEach(function(wave){
        ctx.beginPath(); ctx.moveTo(0,h);
        for(var x=0;x<=w;x+=4){ var nx=x/w; var y=h*wave.yOff+Math.sin(nx*Math.PI*wave.freq+t*wave.speed+mouseInfluence)*wave.amp+Math.sin(nx*Math.PI*wave.freq*2+t*wave.speed*0.5)*wave.amp*0.3; ctx.lineTo(x,y); }
        ctx.lineTo(w,h); ctx.closePath(); ctx.fillStyle=hexToRgba(wave.color,wave.opacity); ctx.fill();
      });
      requestAnimationFrame(draw);
    })();`;

  if (effect === 'starfield') return `    // Starfield
    var ctx = canvas.getContext('2d');
    var w, h, COUNT = ${c.density}, SPEED = ${c.speed};
    var mouse = {x:0.5, y:0.5};
    function hexToRgb(hex){ return [parseInt(hex.slice(1,3),16),parseInt(hex.slice(3,5),16),parseInt(hex.slice(5,7),16)]; }
    function resize(){
      var rect = section.getBoundingClientRect();
      w = rect.width; h = rect.height;
      var dpr = window.devicePixelRatio || 1;
      canvas.width = w * dpr; canvas.height = h * dpr;
      ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
    }
    window.addEventListener('resize', resize); resize();
    section.addEventListener('mousemove', function(e){ var r=section.getBoundingClientRect(); mouse={x:(e.clientX-r.left)/w,y:(e.clientY-r.top)/h}; });
    var stars=[];
    for(var i=0;i<COUNT;i++) stars.push({ x:Math.random()*2-1, y:Math.random()*2-1, z:Math.random(), color:hexToRgb(['${c.colorA}','${c.colorB}','${c.colorC}'][i%3]) });
    (function draw(){
      ctx.fillStyle='${c.bgColor}'; ctx.fillRect(0,0,w,h);
      var cx=w/2,cy=h/2,ox=(mouse.x-0.5)*100,oy=(mouse.y-0.5)*100;
      for(var i=0;i<stars.length;i++){
        var s=stars[i]; s.z-=0.002*SPEED;
        if(s.z<=0){ s.z=1; s.x=Math.random()*2-1; s.y=Math.random()*2-1; }
        var px=cx+(s.x/s.z)*300+ox/s.z, py=cy+(s.y/s.z)*300+oy/s.z;
        var size=(1-s.z)*3, alpha=(1-s.z)*0.8+0.2;
        if(px>0&&px<w&&py>0&&py<h){
          ctx.beginPath(); ctx.arc(px,py,size,0,Math.PI*2);
          ctx.fillStyle='rgba('+s.color[0]+','+s.color[1]+','+s.color[2]+','+alpha+')'; ctx.fill();
        }
      }
      requestAnimationFrame(draw);
    })();`;

  if (effect === 'liquid-aura') return `    // Liquid Aura
    var ctx = canvas.getContext('2d');
    var w, h, time = 0;
    var mouse = {x:0.5, y:0.5};
    function resize(){
      var rect = section.getBoundingClientRect();
      w = rect.width; h = rect.height;
      canvas.width = w * window.devicePixelRatio; canvas.height = h * window.devicePixelRatio;
      ctx.setTransform(window.devicePixelRatio, 0, 0, window.devicePixelRatio, 0, 0);
    }
    window.addEventListener('resize', resize); resize();
    section.addEventListener('mousemove', function(e){ mouse.x = e.clientX/w; mouse.y = e.clientY/h; });
    (function draw(){
      time += 0.005 * ${c.speed};
      ctx.fillStyle = '${c.bgColor}'; ctx.fillRect(0, 0, w, h);
      for(var i=0; i<3; i++){
        var x = w * (0.5 + Math.cos(time + i*2 + mouse.x) * 0.3);
        var y = h * (0.5 + Math.sin(time + i*3 + mouse.y) * 0.3);
        var rad = Math.max(w, h) * 0.8;
        var grad = ctx.createRadialGradient(x, y, 0, x, y, rad);
        var colors = ['${c.colorA}', '${c.colorB}', '${c.colorC}'];
        grad.addColorStop(0, colors[i] + 'aa'); grad.addColorStop(1, 'transparent');
        ctx.globalCompositeOperation = 'screen'; ctx.fillStyle = grad; ctx.fillRect(0, 0, w, h);
      }
      requestAnimationFrame(draw);
    })();`;

  if (effect === 'flow-field') return `    // Flow Field
    var ctx = canvas.getContext('2d');
    var w, h, flowParticles = [];
    var mouse = {x:-100, y:-100};
    function resize(){
      var rect = section.getBoundingClientRect();
      w = rect.width; h = rect.height;
      canvas.width = w * window.devicePixelRatio; canvas.height = h * window.devicePixelRatio;
      ctx.setTransform(window.devicePixelRatio, 0, 0, window.devicePixelRatio, 0, 0);
    }
    window.addEventListener('resize', resize); resize();
    section.addEventListener('mousemove', function(e){ var r=section.getBoundingClientRect(); mouse.x=e.clientX-r.left; mouse.y=e.clientY-r.top; });
    var colorArr = ['${c.colorA}','${c.colorB}','${c.colorC}'];
    for(var i=0; i<${c.density * 5}; i++){
      flowParticles.push({ x:Math.random()*w, y:Math.random()*h, vx:0, vy:0, color:colorArr[Math.floor(Math.random()*3)] });
    }
    (function draw(){
      ctx.fillStyle = '${c.bgColor}1a'; ctx.fillRect(0, 0, w, h);
      flowParticles.forEach(function(p){
        var angle = (Math.sin(p.x * 0.005) + Math.cos(p.y * 0.005)) * Math.PI * 2;
        var dist = Math.hypot(mouse.x - p.x, mouse.y - p.y);
        var finalAngle = dist < 200 ? Math.atan2(mouse.y - p.y, mouse.x - p.x) : angle;
        p.vx += Math.cos(finalAngle) * 0.02 * ${c.speed}; p.vy += Math.sin(finalAngle) * 0.02 * ${c.speed};
        p.x += p.vx; p.y += p.vy; p.vx *= 0.95; p.vy *= 0.95;
        if(p.x<0||p.x>w||p.y<0||p.y>h){ p.x=Math.random()*w; p.y=Math.random()*h; p.vx=0; p.vy=0; }
        ctx.strokeStyle = p.color; ctx.lineWidth = 1.5;
        ctx.beginPath(); ctx.moveTo(p.x, p.y); ctx.lineTo(p.x - p.vx * 2, p.y - p.vy * 2); ctx.stroke();
      });
      requestAnimationFrame(draw);
    })();`;

  if (effect === 'isometric-grid') return `    // Isometric Grid
    var ctx = canvas.getContext('2d');
    var w, h, mouse = {x:0, y:0};
    var size = 30;
    function resize(){
      var rect = section.getBoundingClientRect();
      w = rect.width; h = rect.height;
      canvas.width = w * window.devicePixelRatio; canvas.height = h * window.devicePixelRatio;
      ctx.setTransform(window.devicePixelRatio, 0, 0, window.devicePixelRatio, 0, 0);
    }
    window.addEventListener('resize', resize); resize();
    section.addEventListener('mousemove', function(e){ var r=section.getBoundingClientRect(); mouse.x=e.clientX-r.left; mouse.y=e.clientY-r.top; });
    (function draw(){
      ctx.fillStyle = '${c.bgColor}'; ctx.fillRect(0, 0, w, h);
      var cols = Math.ceil(w / size) + 2, rows = Math.ceil(h / size) + 2;
      for(var i=0; i<cols; i++) for(var j=0; j<rows; j++){
        var x = i * size, y = j * size;
        var dist = Math.hypot(mouse.x - x, mouse.y - y);
        var factor = Math.max(0, 1 - dist / 300);
        ctx.strokeStyle = factor > 0.5 ? '${c.colorA}' : '${c.colorB}44';
        ctx.lineWidth = 0.5 + factor * 2;
        ctx.beginPath();
        ctx.moveTo(x, y - factor * 20 * ${c.speed});
        ctx.lineTo(x + size/2, y + size/4 - factor * 20 * ${c.speed});
        ctx.lineTo(x, y + size/2 - factor * 20 * ${c.speed});
        ctx.lineTo(x - size/2, y + size/4 - factor * 20 * ${c.speed});
        ctx.closePath(); ctx.stroke();
      }
      requestAnimationFrame(draw);
    })();`;

  // aurora-borealis
  return `    // Aurora Borealis
    var ctx = canvas.getContext('2d');
    var w, h, time = 0;
    var mouse = {x:0.5, y:0.5};
    function resize(){
      var rect = section.getBoundingClientRect();
      w = rect.width; h = rect.height;
      canvas.width = w * window.devicePixelRatio; canvas.height = h * window.devicePixelRatio;
      ctx.setTransform(window.devicePixelRatio, 0, 0, window.devicePixelRatio, 0, 0);
    }
    window.addEventListener('resize', resize); resize();
    section.addEventListener('mousemove', function(e){ var r=section.getBoundingClientRect(); mouse.x=(e.clientX-r.left)/w; mouse.y=(e.clientY-r.top)/h; });
    var auroraColors = ['${c.colorA}','${c.colorB}','${c.colorC}'];
    function hexToRgb(hex){ return [parseInt(hex.slice(1,3),16),parseInt(hex.slice(3,5),16),parseInt(hex.slice(5,7),16)]; }
    (function draw(){
      ctx.fillStyle = '${c.bgColor}'; ctx.fillRect(0, 0, w, h); time += 0.01 * ${c.speed};
      for(var i=0; i<3; i++){
        ctx.beginPath(); ctx.moveTo(0, h);
        var rgb = hexToRgb(auroraColors[i]);
        for(var x=0; x<=w; x+=10){
          var nx = x/w, drift = Math.sin(nx * 3 + time + i) * 50;
          var pull = (1 - Math.abs(nx - mouse.x)) * 100 * (1-mouse.y);
          ctx.lineTo(x, h * 0.7 - i*80 + drift - pull);
        }
        ctx.lineTo(w, h);
        var grad = ctx.createLinearGradient(0, h*0.5, 0, h);
        grad.addColorStop(0, 'rgba('+rgb[0]+','+rgb[1]+','+rgb[2]+',0.4)'); grad.addColorStop(1, 'transparent');
        ctx.fillStyle = grad; ctx.fill();
      }
      requestAnimationFrame(draw);
    })();`;
}


