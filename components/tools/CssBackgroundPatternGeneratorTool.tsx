function usePreviewCanvas(
  canvasRef: React.RefObject<HTMLCanvasElement | null>,
  config: EffectConfig,
  effect: EffectType
) {
  const animRef = useRef<number>(0);
  const mouseRef = useRef({ x: -1000, y: -1000 });

  useEffect(() => {
    if (!canvasRef.current) return;
    const canvas = canvasRef.current;
    const ctx = canvas.getContext('2d');
    if (!ctx) return;

    cancelAnimationFrame(animRef.current);

    const w = canvas.offsetWidth;
    const h = canvas.offsetHeight;
    canvas.width = w * 2;
    canvas.height = h * 2;
    ctx.scale(2, 2);

    const onMouseMove = (e: MouseEvent) => {
      const rect = canvas.getBoundingClientRect();
      mouseRef.current = { x: e.clientX - rect.left, y: e.clientY - rect.top };
    };
    const onMouseLeave = () => { mouseRef.current = { x: -1000, y: -1000 }; };
    canvas.addEventListener('mousemove', onMouseMove);
    canvas.addEventListener('mouseleave', onMouseLeave);

    const hexToRgb = (hex: string) => {
      const r = parseInt(hex.slice(1, 3), 16);
      const g = parseInt(hex.slice(3, 5), 16);
      const b = parseInt(hex.slice(5, 7), 16);
      return [r, g, b];
    };

    // ---- PARTICLES ----
    if (effect === 'particles') {
      const colors = [config.colorA, config.colorB, config.colorC];
      const particles = Array.from({ length: config.density }, (_, i) => ({
        x: Math.random() * w, y: Math.random() * h,
        vx: (Math.random() - 0.5) * config.speed * 0.5,
        vy: (Math.random() - 0.5) * config.speed * 0.5,
        r: Math.random() * 2 + 1,
        color: colors[i % 3],
      }));

      const draw = () => {
        ctx.clearRect(0, 0, w, h);
        const bgRgb = hexToRgb(config.bgColor);
        ctx.fillStyle = `rgb(${bgRgb[0]},${bgRgb[1]},${bgRgb[2]})`;
        ctx.fillRect(0, 0, w, h);
        const mouse = mouseRef.current;

        for (let i = 0; i < particles.length; i++) {
          const p = particles[i];
          p.x += p.vx; p.y += p.vy;
          if (p.x < 0 || p.x > w) p.vx *= -1;
          if (p.y < 0 || p.y > h) p.vy *= -1;
          const dx = mouse.x - p.x, dy = mouse.y - p.y;
          const dist = Math.sqrt(dx * dx + dy * dy);
          if (dist < 150) { p.x += dx * 0.01; p.y += dy * 0.01; }

          const rgb = hexToRgb(p.color);
          ctx.beginPath(); ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2);
          ctx.fillStyle = p.color; ctx.fill();

          for (let j = i + 1; j < particles.length; j++) {
            const q = particles[j];
            const ddx = p.x - q.x, ddy = p.y - q.y;
            const d = Math.sqrt(ddx * ddx + ddy * ddy);
            if (d < config.connectDistance) {
              ctx.beginPath(); ctx.moveTo(p.x, p.y); ctx.lineTo(q.x, q.y);
              ctx.strokeStyle = `rgba(${rgb[0]},${rgb[1]},${rgb[2]},${(1 - d / config.connectDistance) * 0.4})`;
              ctx.lineWidth = 0.6; ctx.stroke();
            }
          }
        }
        animRef.current = requestAnimationFrame(draw);
      };
      draw();
    }

    // ---- MATRIX RAIN ----
    if (effect === 'matrix-rain') {
      const fontSize = 14;
      const cols = Math.ceil(w / fontSize);
      const drops = Array.from({ length: cols }, () => Math.random() * -50);
      const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZアイウエオカキクケコ0123456789@#$%^&*';
      const mainRgb = hexToRgb(config.colorA);
      const glowRgb = hexToRgb(config.colorB);
      const bgRgb = hexToRgb(config.bgColor);

      ctx.fillStyle = `rgb(${bgRgb[0]},${bgRgb[1]},${bgRgb[2]})`;
      ctx.fillRect(0, 0, w, h);

      const draw = () => {
        ctx.fillStyle = `rgba(${bgRgb[0]},${bgRgb[1]},${bgRgb[2]},0.05)`;
        ctx.fillRect(0, 0, w, h);
        const mouse = mouseRef.current;

        for (let i = 0; i < cols; i++) {
          const x = i * fontSize, y = drops[i] * fontSize;
          const ch = chars[Math.floor(Math.random() * chars.length)];
          const dx = x - mouse.x, dy = y - mouse.y;
          const dist = Math.sqrt(dx * dx + dy * dy);
          const glow = dist < 120 ? (1 - dist / 120) : 0;

          if (glow > 0.1) {
            ctx.font = `bold ${fontSize + 2}px monospace`;
            ctx.fillStyle = `rgba(${glowRgb[0]},${glowRgb[1]},${glowRgb[2]},${0.9 + glow * 0.1})`;
            ctx.shadowBlur = 20 * glow;
            ctx.shadowColor = config.colorB;
          } else {
            ctx.font = `${fontSize}px monospace`;
            ctx.fillStyle = `rgba(${mainRgb[0]},${mainRgb[1]},${mainRgb[2]},${0.5 + Math.random() * 0.4})`;
            ctx.shadowBlur = 0;
          }
          ctx.fillText(ch, x, y);
          ctx.shadowBlur = 0;
          if (y > h && Math.random() > 0.975) drops[i] = 0;
          drops[i] += config.speed * 0.5;
        }
        animRef.current = requestAnimationFrame(draw);
      };
      draw();
    }

    // ---- WAVES ----
    if (effect === 'waves') {
      let t = 0;
      const waves = [
        { color: config.colorA, amp: 50, freq: 0.8, speed: 1, yOff: 0.6, opacity: 0.4 },
        { color: config.colorB, amp: 40, freq: 1.2, speed: 1.3, yOff: 0.7, opacity: 0.35 },
        { color: config.colorC, amp: 30, freq: 1.6, speed: 0.8, yOff: 0.8, opacity: 0.3 },
        { color: config.colorA, amp: 25, freq: 2.0, speed: 1.5, yOff: 0.85, opacity: 0.25 },
      ];

      const draw = () => {
        ctx.clearRect(0, 0, w, h);
        ctx.fillStyle = config.bgColor;
        ctx.fillRect(0, 0, w, h);
        t += config.speed * 0.01;
        const mouseInfluence = ((mouseRef.current.x / w) - 0.5) * 30;

        for (const wave of waves) {
          const rgb = hexToRgb(wave.color);
          ctx.beginPath();
          ctx.moveTo(0, h);
          for (let x = 0; x <= w; x += 4) {
            const nx = x / w;
            const y = h * wave.yOff
              + Math.sin(nx * Math.PI * wave.freq + t * wave.speed + mouseInfluence) * wave.amp
              + Math.sin(nx * Math.PI * wave.freq * 2 + t * wave.speed * 0.5) * wave.amp * 0.3;
            ctx.lineTo(x, y);
          }
          ctx.lineTo(w, h);
          ctx.closePath();
          ctx.fillStyle = `rgba(${rgb[0]},${rgb[1]},${rgb[2]},${wave.opacity})`;
          ctx.fill();
        }
        animRef.current = requestAnimationFrame(draw);
      };
      draw();
    }

    // ---- STARFIELD ----
    if (effect === 'starfield') {
      const starColors = [config.colorA, config.colorB, config.colorC];
      const stars = Array.from({ length: config.density }, (_, i) => ({
        x: Math.random() * 2 - 1, y: Math.random() * 2 - 1, z: Math.random(),
        color: hexToRgb(starColors[i % 3]),
      }));

      const draw = () => {
        ctx.fillStyle = config.bgColor;
        ctx.fillRect(0, 0, w, h);
        const cx = w / 2, cy = h / 2;
        const mx = (mouseRef.current.x > -500 ? mouseRef.current.x / w : 0.5);
        const my = (mouseRef.current.y > -500 ? mouseRef.current.y / h : 0.5);
        const ox = (mx - 0.5) * 100, oy = (my - 0.5) * 100;

        for (const s of stars) {
          s.z -= 0.002 * config.speed;
          if (s.z <= 0) { s.z = 1; s.x = Math.random() * 2 - 1; s.y = Math.random() * 2 - 1; }
          const px = cx + (s.x / s.z) * 300 + ox / s.z;
          const py = cy + (s.y / s.z) * 300 + oy / s.z;
          const size = (1 - s.z) * 3;
          const alpha = (1 - s.z) * 0.8 + 0.2;
          if (px > 0 && px < w && py > 0 && py < h) {
            ctx.beginPath(); ctx.arc(px, py, size, 0, Math.PI * 2);
            ctx.fillStyle = `rgba(${s.color[0]},${s.color[1]},${s.color[2]},${alpha})`;
            ctx.fill();
            ctx.beginPath(); ctx.arc(px, py, size * 3, 0, Math.PI * 2);
            ctx.fillStyle = `rgba(${s.color[0]},${s.color[1]},${s.color[2]},${alpha * 0.1})`;
            ctx.fill();
          }
        }
        animRef.current = requestAnimationFrame(draw);
      };
      draw();
    }

    return () => {
      cancelAnimationFrame(animRef.current);
      canvas.removeEventListener('mousemove', onMouseMove);
      canvas.removeEventListener('mouseleave', onMouseLeave);
    };
  }, [effect, config, canvasRef]);
}

