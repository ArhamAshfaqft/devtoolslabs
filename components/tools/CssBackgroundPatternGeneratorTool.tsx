// --- Types ---
type EffectType = 'particles' | 'gradient-blobs' | 'matrix-rain' | 'waves' | 'starfield' | 'liquid-aura' | 'flow-field' | 'isometric-grid' | 'aurora-borealis';
type PlacementType = 'page' | 'container' | 'selector';

interface EffectConfig {
  colorA: string;
  colorB: string;
  colorC: string;
  bgColor: string;
  density: number;
  speed: number;
  connectDistance: number;
  placement?: PlacementType;
  customSelector?: string;
}

// --- Integration Helpers ---
function getWrapperStyles(c: EffectConfig): string {
  const isPage = !c.placement || c.placement === 'page';
  return `position:${isPage ? 'fixed' : 'absolute'};top:0;left:0;width:100%;height:100%;z-index:-1;background:${c.bgColor};overflow:hidden;pointer-events:none;`;
}

function getInitScript(canvasId: string, c: EffectConfig): string {
  if (!c.placement || c.placement === 'page') return `const canvas = document.getElementById('${canvasId}');`;
  
  const target = c.placement === 'selector' ? `document.querySelector('${c.customSelector}')` : `document.currentScript.parentElement.closest('.elementor-section, .elementor-container, section, div')`;
  
  return `
  const canvas = document.getElementById('${canvasId}');
  const container = ${target};
  if (container) {
    container.style.position = 'relative';
    container.style.overflow = 'hidden';
    if (container.offsetWidth) {
      canvas.width = container.offsetWidth;
      canvas.height = container.offsetHeight;
    }
  }
  `;
}

// --- Elementor-Native Helpers ---
